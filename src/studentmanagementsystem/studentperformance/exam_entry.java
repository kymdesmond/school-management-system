    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jcat1.getText().equals("")||jcat2.getText().equals("")||jexam.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Enter Respective Scores");
        }
        else if(Integer.parseInt(jcat1.getText())>30||Integer.parseInt(jcat2.getText())>30||Integer.parseInt(jexam.getText())>70)
                {
                    JOptionPane.showMessageDialog(null,"Enter Correct Marks.\n CATs=<30\n Exams =<70");
                }
        else{
        double Total;
        String Grade;
        Total=Integer.parseInt(jcat1.getText())+Integer.parseInt(jcat2.getText())+Integer.parseInt(jexam.getText());
        if(Total>69)
        {
            Grade="A";
        }
        else if(Total>59)
                {
                  Grade="B";
                }
        else if(Total>49)
                {
                  Grade="C";
                }
        else if(Total>39)
                {
                  Grade="D";
                }
        else
                {
                  Grade="E";
                }
        String query="INSERT INTO sms.student_performance(registration_id, cat1, cat2, exam, total, grade) values"
        + "('"+jregistrationid.getSelectedItem()+"',"
        + "'"+jcat1.getText()+"',"
        + "'"+jcat2.getText()+"',"
        + "'"+jexam.getText()+"','"+Total+"','"+Grade+"')";
        executeQuery(query, "inserted");
        jcat1.setText("");
        jcat2.setText("");
        jexam.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed
    }
