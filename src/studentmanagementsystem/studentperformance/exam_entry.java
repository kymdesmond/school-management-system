    public ArrayList<examsclass>getstudentperformance()
{
    ArrayList<examsclass> studentslist=new ArrayList<examsclass>();
    Connection connection=dbconnect.ConnectDB();
    String query="SELECT * FROM student_performance";
    Statement pst;
    ResultSet rst;
    
    try{
        pst=connection.createStatement();
        rst=pst.executeQuery(query);
        
        examsclass student;
        while(rst.next())
        {
            student=new examsclass(rst.getString("registration_id"),rst.getString("cat1"),rst.getString("cat2"),rst.getString("exam"),rst.getString("total"),rst.getString("grade"));
            studentslist.add(student);
        }
    }
    catch(Exception e)
    {
        e.printStackTrace();
    }
    return studentslist;
}
    public void display()
{
    ArrayList<examsclass> list=getstudentperformance();
    DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
    Object[]row=new Object[6];
    for(int i=0;i<list.size();i++)
    {
        row[0]=list.get(i).getid();
        row[1]=list.get(i).getcat1();
        row[2]=list.get(i).getcat2();
        row[3]=list.get(i).getexam();
        row[4]=list.get(i).gettotal();
        row[5]=list.get(i).getgrade();
        model.addRow(row);
    }
}
 public void executeQuery(String query,String message)
{
    Connection conn=dbconnect.ConnectDB();
    Statement st;
    try
    {
        st=conn.createStatement();
        if((st.executeUpdate(query)) ==1)
        {
            //refresh table
            DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            display();
            JOptionPane.showMessageDialog(null, "Data"+message+"Successfully");
        }
        else{
            JOptionPane.showMessageDialog(null, "Data Not"+message);
        }
        
    }
    catch(Exception ex){
        ex.printStackTrace();
    }
}
